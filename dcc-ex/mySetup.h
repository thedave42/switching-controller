// mySetup.h — DCC-EX turnout definitions for Switching Controller
//
// Copy this file to your EX-CommandStation source folder.
// Each SETUP() command defines a turnout on a Switching Controller VPIN.
//
// Control from any throttle or serial monitor:
//   <T 1 T>   — Throw turnout T01
//   <T 1 C>   — Close turnout T01
//   <JT>      — List all turnout IDs

SETUP("<T 1 VPIN 800>");    // T01
SETUP("<T 2 VPIN 801>");    // T02
SETUP("<T 3 VPIN 802>");    // T03
SETUP("<T 4 VPIN 803>");    // T04
SETUP("<T 5 VPIN 804>");    // T05
SETUP("<T 6 VPIN 805>");    // T06
SETUP("<T 7 VPIN 806>");    // T07
SETUP("<T 8 VPIN 807>");    // T08
SETUP("<T 9 VPIN 808>");    // T09
SETUP("<T 10 VPIN 809>");   // T10
SETUP("<T 11 VPIN 810>");   // T11
SETUP("<T 12 VPIN 811>");   // T12
